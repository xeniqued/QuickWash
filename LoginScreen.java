    private void signInActionPerformed(java.awt.event.ActionEvent evt) {
        // Get the values from the text fields
        String userID = jTextField1.getText();
        String passwordChars = String.valueOf(jPasswordField1.getPassword());
        
        int minLength = 7;
        int idNumber;
    
        PersonList.readFromFile();

        //Check if both password and ID number are both short or empty
        if (passwordChars.length() < minLength || userID.length() < minLength){
            JOptionPane.showMessageDialog(this, "Both ID number and Password must be at least " + minLength + " characters long.", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Stop processing if ID number is too short
        }
    
        try {
            idNumber = Integer.parseInt(userID);
            // Check if the ID number is too short
            if (userID.length() < minLength) {
                JOptionPane.showMessageDialog(this, "ID number must be at least " + minLength + " characters long.", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Stop processing if ID number is too short
            }
        } catch (NumberFormatException e) {
            // Check if the ID number is not a valid integer
            JOptionPane.showMessageDialog(this, "ID number must be a valid integer.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Stop processing if ID number is not an integer
        }
    
        
        boolean passConf = PersonList.idPasswordMatching(userID, passwordChars);

        if (passConf == true){
            // Successful Login
            JOptionPane.showMessageDialog(this, "Login Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else{
        // Unsuccessful login
            JOptionPane.showMessageDialog(this, "Password or Username not found", "Failure", JOptionPane.INFORMATION_MESSAGE);
        }
        
        // Dispose the current frame
        dispose();
    }
    

